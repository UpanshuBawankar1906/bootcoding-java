public class Increment 
{

	public static void main(String[] args) 
       {

		int a = 9;
			
                System.out.println("Original value of the variable = " + a);

		a++; 	 
		System.out.println("a++ = " + a);

		++a;		
		System.out.println("++a = " + a);
             
                a--;
		System.out.println("a-- = " + a);
            
                --a;
		System.out.println("--a = " + a); 

                
	}
}