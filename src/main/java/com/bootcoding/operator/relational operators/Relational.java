public class Relational
{
public static void main(String [] args)
  { 
      int a = 7;
      int b = 3;
      int c = 8;
 
      System.out.println("7 > 3 = " + (a > b));
      System.out.println("7 < 3 = " + (a < b));
      System.out.println("7 <= 3 = " + (a <= b));
      System.out.println("8 >= b = " + (c >= b));
      System.out.println("7 == b = " + (a == b));
      System.out.println("8 != b = " + (c != b));

  }
}