public class TrafficLight
{
   public static void main(String [] args)
     {
        
        String color = "red";
     

        switch(color)
          {
              case "red":
              System.out.println("Green");
              break;

              case "green":
              System.out.println("Yellow");
              break;

              case "yellow":
              System.out.println("Red");
              break;
         }
     }
}